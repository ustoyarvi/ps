/*
 * Применяет функцию f к каждому символу строки s, передавая индекс каждого
 * символа в качестве аргумента вместе с самим символом.
 */

#include "../libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	if (s == NULL || f == NULL)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		(*f)(i, s + i);
		i++;
	}
}
